package parte3.ejercicio2;

public class Media {
	/**
	 * 
	 * @param a
	 * @param b
	 * @return media entre a y b
	 */
	public double media(int a, int b) {
		return (double)(a + b) / 2;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return media entre a y b
	 */
	public double media(int a, int b, int c) {
		return (double)(a + b + c) / 3;
	}
}
