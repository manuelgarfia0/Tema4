package parte3.ejercicio1;

public class Suma {
	/**
	 * 
	 * @param a
	 * @param b
	 * @return suma entre a y b
	 */
	public int suma(int a, int b) {
		return a + b;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return suma entre a y b
	 */
	public double suma(double a, double b) {
		return a + b;
	}
}
