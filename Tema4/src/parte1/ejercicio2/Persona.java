package parte1.ejercicio2;

public class Persona {

	String DNI = "";
	String nombre = "";
	String apellidos = "";
	int edad = 0;
	// Constructor
	public Persona(String DNI, String nombre, String apellidos, int edad) {
		this.DNI = DNI;
		this.nombre = nombre;
		this.apellidos = apellidos;
		this.edad = edad;
	}

}
